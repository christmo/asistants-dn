package ec.com.dinersclub.chatbot.flowalexa.exception;

public class OtpException extends Exception {

	/**
	 * Serial generado.
	 */
	private static final long serialVersionUID = 5665213611826888432L;

	public OtpException() {
	}

	public OtpException(String message) {
		super(message);
	}

	public OtpException(Throwable cause) {
		super(cause);
	}

	public OtpException(String message, Throwable cause) {
		super(message, cause);
	}

	public OtpException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
