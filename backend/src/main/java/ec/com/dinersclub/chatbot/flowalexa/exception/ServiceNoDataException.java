package ec.com.dinersclub.chatbot.flowalexa.exception;

public class ServiceNoDataException extends Exception {

	/**
	 * Serial generado.
	 */
	private static final long serialVersionUID = 4306198074074856720L;

	public ServiceNoDataException() {
	}

	public ServiceNoDataException(String arg0) {
		super(arg0);
	}

	public ServiceNoDataException(Throwable arg0) {
		super(arg0);
	}

	public ServiceNoDataException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ServiceNoDataException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
